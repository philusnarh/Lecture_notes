n = 500
dates = pd.date_range(start="1980-01-01", periods=n, freq="M")
t = np.arange(n)

signal = (
    50
    + 0.05 * t
    + 10 * np.sin(2 * np.pi * t / 12)
    + np.random.normal(0, 2, n)
)

df = pd.DataFrame({"date": dates, "temp": signal})

# ------------------------------------------------------------
# 2. Create three random non‑overlapping missing gaps
# ------------------------------------------------------------
def random_non_overlapping_gaps(n_points, n_gaps=3, min_len=15, max_len=50, random_state=42):
    rng = np.random.RandomState(random_state)
    gaps = []
    attempts = 0
    while len(gaps) < n_gaps and attempts < 1000:
        start = rng.randint(50, n_points - max_len - 10)
