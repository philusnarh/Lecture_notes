from sklearn.linear_model import LinearRegression

# ------------------------------------------------------------
# Prepare training data (non‑missing indices)
# ------------------------------------------------------------
train_mask = df_missing["temp"].notna().values
t_train = t[train_mask].reshape(-1, 1)
y_train = df_missing.loc[train_mask, "temp"].values

# ------------------------------------------------------------
# Build Fourier features
# ------------------------------------------------------------
def fourier_features(t_vals, period=12, n_harmonics=2):
    features = []
    for k in range(1, n_harmonics + 1):
        features.append(np.sin(2 * np.pi * k * t_vals / period))
        features.append(np.cos(2 * np.pi * k * t_vals / period))
    return np.column_stack(features)

X_train = np.column_stack([
    t_train,
    fourier_features(t_train.ravel())
])

# ------------------------------------------------------------
# Fit model
# ------------------------------------------------------------
model = LinearRegression()
model.fit(X_train, y_train)

# ------------------------------------------------------------
# Predict missing gap
# ------------------------------------------------------------
missing_indices = list(range(missing_start, missing_end + 1))   # explicit list
t_missing = t[missing_indices].reshape(-1, 1)
X_missing = np.column_stack([
    t_missing,
    fourier_features(t_missing.ravel())
])
y_pred = model.predict(X_missing)   # 1D array, length = len(missing_indices)

# ------------------------------------------------------------
# Reconstruct
# ------------------------------------------------------------
df_reconstructed = df_missing.copy()
df_reconstructed.iloc[missing_indices, df_reconstructed.columns.get_loc("temp")] = y_pred

# ------------------------------------------------------------
# Plot
# ------------------------------------------------------------
