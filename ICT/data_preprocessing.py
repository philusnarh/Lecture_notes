# ============================================================
# 2. OUTLIER DETECTION & CORRECTION
# ============================================================
def detect_outliers_mad(data, threshold=2.5):
    """Return boolean mask of outliers using MAD with lower threshold."""
    median = np.median(data)
    mad = np.median(np.abs(data - median))
    if mad == 0:
        return np.zeros(len(data), dtype=bool)
    z_scores = 0.6745 * (data - median) / mad
    return np.abs(z_scores) > threshold

def correct_outliers_winsorize(data, outliers_mask, lower_percentile=1, upper_percentile=99):
    """Replace outliers with specified percentiles of the clean data."""
    corrected = data.copy()
    if not outliers_mask.any():
        return corrected
    clean = data[~outliers_mask]
    if len(clean) == 0:
        return corrected
    low_val = np.percentile(clean, lower_percentile)
    high_val = np.percentile(clean, upper_percentile)
    corrected[outliers_mask & (data < low_val)] = low_val
    corrected[outliers_mask & (data > high_val)] = high_val
    return corrected

# Detect outliers with different thresholds for raw and log
raw_outliers = detect_outliers_mad(raw, threshold=2.5)   # more sensitive
log_outliers = detect_outliers_mad(log, threshold=2.0)   # even more sensitive on log scale

# Correct (use tighter percentiles to force change)
raw_corrected = correct_outliers_winsorize(raw, raw_outliers, lower_percentile=5, upper_percentile=95)
log_corrected = correct_outliers_winsorize(log, log_outliers, lower_percentile=5, upper_percentile=95)

print(f"Raw outliers detected: {raw_outliers.sum()} (threshold=2.5 MAD)")
