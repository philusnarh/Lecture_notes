import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

np.random.seed(123)  # reproducible results
dates = pd.date_range("2000-01-01", periods=500, freq="D")

# ============================================================
# GENERATE SYMMETRIC DATA (NORMAL, NO OUTLIERS)
# ============================================================
n = 500
# Purely normal distribution – symmetric, no outliers
values = np.random.normal(loc=50, scale=10, size=n)  # mean=50, std=10

# Add 25% random missing values (similar proportion as before)
missing = np.random.choice([True, False], size=n, p=[0.45, 0.55])
df = pd.DataFrame({"date": dates, "original": values})
df["with_missing"] = df["original"].where(~missing, np.nan)

# ============================================================
# IMPUTATION: MEAN vs MEDIAN
# ============================================================
mean_val = df["with_missing"].mean()
median_val = df["with_missing"].median()
df["mean_imp"] = df["with_missing"].fillna(mean_val)
df["median_imp"] = df["with_missing"].fillna(median_val)

# For symmetric data, we don't have outliers; "bulk" means all points
mask_missing = df["with_missing"].isna()

# Errors on ALL missing points
rmse_mean_all = np.sqrt(((df.loc[mask_missing, "original"] - mean_val)**2).mean())
rmse_median_all = np.sqrt(((df.loc[mask_missing, "original"] - median_val)**2).mean())

# Also compute errors on "bulk" = all missing (no outlier separation needed)
# But we keep the same variable names for consistency
bulk_missing = mask_missing  # all missing are "bulk"
rmse_mean_bulk = rmse_mean_all
rmse_median_bulk = rmse_median_all
ratio = rmse_mean_bulk / rmse_median_bulk

# ============================================================
# PRINT RESULTS
# ============================================================
print("="*60)
print("DATA: Symmetric (Normal) – no outliers")
print("="*60)
print(f"Mean of observed data = {mean_val:.2f}")
print(f"Median of observed data = {median_val:.2f}")
print(f"(Mean and median are nearly equal – distribution symmetric)")
print(f"\n--- All missing points ---")
print(f"RMSE mean imputation   = {rmse_mean_all:.2f}")
print(f"RMSE median imputation = {rmse_median_all:.2f}")
if ratio < 1:
    print(f"\n→ Mean imputation error is {1/ratio:.1f}x smaller than median.")
elif ratio > 1:
    print(f"\n→ Median imputation error is {ratio:.1f}x smaller than mean.")
else:
    print("\n→ Both methods perform identically.")
print("\n** For symmetric data, mean imputation works well (low error). **")

